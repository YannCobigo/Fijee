float 
DDi::transformed_distance(const Point_vector& P1, const Point_vector& P2) const {
  float distx = P1.x() - P2.x();
  float disty = P1.y() - P2.y();
  float distz = P1.z() - P2.z();
  //
  return distx * distx + disty * disty + distz * distz;
}

